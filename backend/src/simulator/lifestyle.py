def _get_demographic_value(
    demographics: dict[str, str],
    dimension: str,
) -> str | None:
    """Get the demographic value for a modifier dimension."""
    return demographics.get(dimension)


def assign_lifestyle_tags(
    age: int,
    gender: str,
    region: str,
    income_level: str,
    household_type: str,
    rng: "random.Random",
) -> list[str]:
    """Assign lifestyle tags to an agent based on conditional prevalence.

    Each tag has a base_rate modified by demographic-conditional multipliers.
    Final probability = base_rate × product(applicable_modifiers), clamped to [0, 1].

    Args:
        age: Agent age (used to determine age_band)
        gender: "male" or "female"
        region: Region enum value string
        income_level: "low", "middle", or "high"
        household_type: Household type enum value string
        rng: Random number generator for reproducibility

    Returns:
        List of assigned tag IDs
    """
    # Determine age band
    if age < 30:
        age_band = "young"
    elif age < 50:
        age_band = "middle"
    else:
        age_band = "senior"

    demographics = {
        "age_band": age_band,
        "gender": gender,
        "region": region,
        "income_level": income_level,
        "household_type": household_type,
    }

    tags: list[str] = []
    for tag_id, tag_def in LIFESTYLE_TAGS.items():
        prob = tag_def.base_rate

        for key, multiplier in tag_def.modifiers.items():
            dim, val = key.split(":", 1)
            agent_val = demographics.get(dim)
            if agent_val == val:
                prob *= multiplier

        prob = max(0.0, min(1.0, prob))
        if rng.random() < prob:
            tags.append(tag_id)

    return tags
