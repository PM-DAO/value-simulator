LIFESTYLE_TAGS: dict[str, LifestyleTagDef] = {
    d["id"]: LifestyleTagDef(**d) for d in _TAGS
}

ALL_TAG_IDS: list[str] = list(LIFESTYLE_TAGS.keys())

# Group → subgroup → [tag_ids] for LLM prompt construction
TAG_HIERARCHY: dict[str, dict[str, list[str]]] = {}
for tag in LIFESTYLE_TAGS.values():
    TAG_HIERARCHY.setdefault(tag.group, {}).setdefault(tag.subgroup, []).append(tag.id)

# Flat group → [tag_ids]
TAG_GROUPS: dict[str, list[str]] = {}
for tag in LIFESTYLE_TAGS.values():
    TAG_GROUPS.setdefault(tag.group, []).append(tag.id)


def build_compact_tag_prompt() -> str:
    """Build a compact hierarchical tag listing for LLM prompts.

    Format: [group] subgroup: tag1, tag2, tag3 | subgroup2: tag4, tag5
    """
    lines = []
    for group, subgroups in TAG_HIERARCHY.items():
        parts = []
        for subgroup, tag_ids in subgroups.items():
            parts.append(f"{subgroup}: {', '.join(tag_ids)}")
        lines.append(f"[{group}] {' | '.join(parts)}")
    return "\n".join(lines)


